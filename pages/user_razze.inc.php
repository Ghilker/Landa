            Descrizione: 
<b>Allineamenti possibili</b>
Buono

<b>Descrizione generale</b>
Degli Aasimar non si conosce molto… Non si sa quasi nulla, a dire il vero, se non sporadiche e molto spesso fantasiose leggende. Quel poco che circola su di loro, favoleggia su esseri di luce, dotati di grandi ali bianche e con poteri straordinari. Altre dicerie sembrano invece preferire concentrarsi sulla bellezza di queste creature, eteree e perfette, così maestose e lucenti da lasciare senza fiato. Persino il reale nome di questa razza s’è quasi del tutto perso lungo le straordinarie storie che si tramandano su di loro, e il più delle volte ci si riferisce impropriamente a loro come “angeli bianchi”, creduti creature immortali provenienti da un altro mondo, la cui comparsa è solitamente sinonimo di lieto evento.
La realtà si discosta spesse volte dal mito: gli Aasimar infatti non sono né creature mitologiche né esseri divini, bensì lo straordinario frutto dell’unione tra un vivente e uno spirito dell’Empireo. Tale fusione può avvenire per diversi motivi: rituali specifici o possessioni volontarie da parte dello spirito sono le condizioni più frequenti, tuttavia generalmente, a prescindere dalle modalità, v’è il consenso cosciente da entrambe le parti. La creazione di un Aasimar permette da un lato allo spirito di permanere in maniera stabile nel mondo terreno, grazie al legame a doppio filo che lo ancora al corpo che ha scelto di abitare, dall’altro concede al vivente una serie di capacità e poteri derivanti dall’energia dello spirito stesso che, da lì in avanti, accompagnerà la sua esistenza.
Solitamente, ogni volta che un vivente si trasforma in Aasimar, sul suo corpo compare un marchio più o meno visibile: esso è un glifo che rappresenta l’unione tra spirito e vivente, lo snodo tra vita mortale e essenza spirituale, il punto nevralgico dell’intera unione. Appare come una sorta di tatuaggio, e generalmente l’Aasimar tende ad essere molto sensibile in quella zona, e per questo è raro che lasci toccare ad altri il proprio marchio. 

<b>Carattere</b>
Gli Aasimar sono per definizione creature buone, giacché solamente un vivente già incline al bene è in grado d’accogliere in sé l’essenza di un Patrono. Questo, tuttavia, non significa che siano tutti dei misericordiosi o innocenti angioletti, anzi: ognuno interpreta la ricerca del bene a modo proprio. Pertanto esistono certamente Aasimar gioviali, solari, generosi, che amano giocare con i bambini o far ridere le persone, altri che si ergono a difensori dei deboli e degli innocenti, valorosi e puri di cuore… Altri ancora, invece, possono anche risultare freddi, distaccati, all’apparenza persino insensibili, pronti a infliggere dolore e sacrifici qualora necessari per un bene superiore. Tuttavia nessun Aasimar trarrà mai piacere personale dalla morte o dalla sofferenza altrui, e anche qualora fosse per loro necessario imporle, proveranno sempre una certa dose di costernazione interiore. 
Sebbene la bontà di ciascun Aasimar sia un tratto piuttosto variabile e interpretabile, vi sono altre caratteristiche che tendono a ricorrere in maniera più stabile in ciascun Aasimar che si rispetti:

- Coraggio: il potere spirituale dona al fu vivente poteri sovrumani, che lascerebbero di stucco anche le più fervide immaginazioni. Questo infonde nel cuore di ogni Aasimar un imprescindibile coraggio: nessun Aasimar degno di questo nome si fregerà mai della nomea di “fifone”, e spesso la loro indole impavida diviene anche motivo di sprone per coloro che li circondano. Questo non significa che queste creature non possano provare paura, ma generalmente avviene solo per motivazioni più che giustificate, ed è comunque molto difficile che si sottraggano ai propri doveri per mero e semplice timore.

- Senso del dovere: nel momento in cui un Patrono si fonde a un vivente, v’è sempre uno scopo a muovere le fila di tale fusione. “La Missione”, la chiamano in molti tra questa razza celestiale: ogni Aasimar ha nella propria indole l’istinto di perseguire un obiettivo ben preciso, che ha sempre a che fare con il mantenimento del bene (ad esempio proteggere un villaggio vessato dal male, porre fine a una guerra sanguinaria, neutralizzare un demone in libera circolazione, etc.). Coloro che rimangono senza una missione da compiere, vivono in uno stato di inquietudine e irrequietezza finché non troveranno un altro scopo da perseguire. In generale, è molto difficile che qualcosa (o qualcuno) riesca a far desistere un Aasimar da quelli che lui o lei percepisce come i propri doveri.

- Calma: è una qualità tipica dei Patroni, che di conseguenza si riflette anche sul vivente che posseggono. Gli Aasimar sono individui costantemente circondati da un’aura di calma e pazienza, è difficile far perdere loro le staffe e altrettanto raro è vederli agire d’impulso o frettolosamente. Sono molto riflessivi, ponderano bene ciò che è meglio fare in ogni situazione e si affidano principalmente alla propria razionalità per scegliere la via migliore da percorrere.

- Indole razionale: come diretta conseguenza della fusione con un Patrono, gli Aasimar tendono a guardare il mondo con un occhio estremamente razionale, che alcuni interpretano come freddo e a volte persino cinico. Lo status indubbiamente superiore di queste creature rispetto ai comuni mortali pone spesso un certo distacco tra le due parti, e per quanto un Aasimar permanga sempre incline ad aiutare e difendere gli abitanti del mondo terreno, è più difficile che se ne lasci coinvolgere emotivamente. Questo non significa che essi non possano provare emozioni o persino sentimenti per quelli diversi da loro, semplicemente sarà (molto) più difficile far provare loro affetto profondo per un normale vivente. Quando questo accade, tuttavia, risulta essere un sentimento molto difficile da estirpare.

- Altruismo: un Aasimar è sempre ben lieto di poter aiutare il prossimo, e trae piacere personale nell’essere utile o fare la felicità di qualcun altro. Quest’inclinazione naturale può essere tuttavia interpretata molto diversamente da individuo a individuo: alcuni instillano il proprio altruismo in azioni nobili, valorose, eroiche, gloriose, di quelle che i bardi amano narrare nelle proprie canzoni. Per altri, invece, l’altruismo si esplica nei piccoli gesti, nelle azioni quotidiane in grado di scucire un sorriso, nella semplicità di un abbraccio o nel calore di qualche parola gentile. In generale, in ogni caso, è estremamente raro che le azioni di un Aasimar siano mosse da scopi puramente personali ed egoistici (come, ad esempio, la ricerca di potere o di ricchezza), poiché non rispetta la natura della sua esistenza. 

- Avversione verso il male: questo sentimento è alla radice della natura stessa di un Aasimar. Esso è luce, è bene, e come tale è incompatibile con le ombre e con il male. Un Aasimar detesta qualsiasi forma di malvagità, ne è istintivamente disgustato, ma il modo attraverso cui ognuno di loro sceglie di agire in merito, può variare da individuo a individuo: per alcuni Aasimar infatti il male (e le creature che lo servono) sono semplicemente dei parassiti da estirpare, una piaga da sanare affinché il mondo possa vivere in pace. Altri, invece, preferiscono intraprendere la via (per certi versi più ardua) della conversione: vedono ai malvagi come creature “rotte”, malfunzionanti, e pertanto possibili da aggiustare. Cercheranno dunque di “guarirli” dalle loro tenebre, di estirpare in loro intenti malvagi, sino a renderli… Beh, quantomeno neutrali. Alcuni Aasimar si incaponiscono così tanto su tale obiettivo da rendere la conversione di cattivi in buoni una vera e propria missione di vita. Altri, invece, più semplicemente decidono il modo migliore attraverso cui approcciarsi alle ombre in base alla singola situazione.

<b>Forme razziali</b>
Gli Aasimar sono il risultato dell’unione di un vivente con uno spirito: questo, in una certa misura, influenza anche il loro aspetto. Nel dettaglio, un Aasimar è in grado di mutare la propria forma e assumerne una che rende ragione dello spirito che custodisce nel proprio corpo: la Forma Celestiale. Egli è in grado di variare da una forma all’altra secondo propria volontà: sarà sufficiente prendersi qualche minuto per isolarsi dal mondo e concentrarsi sul proprio corpo, focalizzandosi con una delle due componenti preponderanti del proprio organismo (sangue vivente o energia spirituale) e con essa sincronizzarsi [1T - Sincronizzazione]. Una volta preso contatto con una delle due componenti, sarà sufficiente evocarla così che essa possa emergere e determinare l’aspetto dell’Aasimar: sangue vivente per la Forma Umana, energia spirituale per la Forma Celestiale [2T - Trasformazione].

- Forma Vivente (F.V.): è la forma naturale che il vivente possedeva prima di trasformarsi in Aasimar. Non vi sono differenze sostanziali, se non in piccoli dettagli che tuttavia non stravolgono in alcun modo i connotati dell’individuo: l’aspetto si mostra generalmente più luminoso, l’incarnato tende a essere più chiaro, levigato e immacolato, i capelli più lucenti e gli occhi più brillanti. 
N.B. Razze come maridi e farie che già per natura posseggono due forme di razza differenti, possono mantenere entrambi gli aspetti, con le medesime modalità di trasformazione da una forma all’altra di prima che divenissero Aasimar. Perdono, tuttavia, qualsiasi tipo di bonus razziale inerente alla razza precedente e qualsiasi tipo di vantaggio possa derivare dalla loro forma originale. 

- Forma Celestiale (F.C.): è la forma in cui l’energia spirituale emerge, uscendo dal proprio stato latente e divenendo preponderante. In questa forma le sembianze dell’Aasimar si fanno ancora più chiare e luminose, persino eteree, e la sua intera figura viene circondata da una sottile e tenue aura luminosa. Ma quel che è più importante e appariscente, è il sorgere di un paio di estensioni spirituali tra le sue scapole: sono due ali. Esse permettono all’Aasimar di volare: generalmente queste hanno la necessità di sbattere qualche volta, gonfiandosi d’aria, prima di sollevare il peso dell’Aasimar in volo, a meno che quest’ultimo non salti nel vuoto o spicchi un balzo sufficientemente alto e potente: in tal caso, il volo sarà immediato. E’ inoltre per loro possibile trasportare in volo al massimo un’altra persona, o oggetti pari al loro stesso peso. Le ali, tuttavia, sono spesso sfruttate anche per altri scopi: sono molto resistenti, e per questo sono spesso utilizzate a mo’ di scudo protettivo oppure come estensione del proprio corpo per colpire l’avversario e sbalzarlo alcuni metri più in là.
N.B. La Forma Celestiale, per razze che in precedenza erano maridi o faerie, è evocabile solamente a partire dalla loro Forma Umana.

<b>Descrizione fisica</b>

FORMA VIVENTE
Viso: analogo a quello precedente alla fusione col Patrono. Le imperfezioni della pelle (come rughe, acne o cicatrici) a volte si levigano fino quasi a scomparire, l’incarnato diviene spesso più limpido e luminoso, i capelli tendono a tinte più chiare e lucenti e le iridi assumono spesso colorazioni più brillanti.

Corpo: il corpo rimane grossomodo lo stesso precedente alla trasformazione in Aasimar, al massimo può risultare più tonico e armonioso rispetto a prima, ma senza comunque stravolgere la fisionomia originale.

Altezza: analoga a quella precedente alla trasformazione in Aasimar.

Peso: analogo a quello precedente alla trasformazione in Aasimar.

FORMA CELESTIALE
Viso: la pelle in questa forma emana un sottile e tenue bagliore che circonda l’intera figura dell’Aasimar, e non presenta la benché minima traccia di imperfezioni o inestetismi. E’ anzi liscia, immacolata, come quella di una creatura ultraterrena (che, in effetti, in parte è). Anche i capelli risultano ancora più lucenti, come fili sottili di un qualche prezioso metallo. Gli occhi, tuttavia, sono ciò che più di tutti risplendono di luce: a prescindere dal colore originale, in forma celeste le iridi si addensano di energia spirituale, accendendosi di piccole e fioche scintille che rendono lo sguardo dell’Aasimar suggestivo e, stando ai racconti, “simile a quello di un dio”. Che poi, gli Dèi nemmeno esistono.

Corpo: il corpo in Forma Celestiale appare più prestante e performante rispetto alla Forma Vivente, senza tuttavia stravolgerne i connotati in maniera eccessiva. Capita spesso che in questa forma gli Aasimar siano un pochino più alti e più grandi rispetto alla forma umana, rispettando tuttavia le proporzioni originarie. La cosa che caratterizza davvero questa forma, tuttavia, sono le ali,  le quali possono assumere fattezze lievemente diverse in base all’Aasimar stesso: a volte altro non sono che propaggini luminose costituite di pura energia, altre volte presentano la foggia di un paio di maestose ali piumate, altre volte ancora assomigliano più ad ali membranose. Le costanti, tuttavia, sono l’aspetto solenne e le tinte chiare tendenti al bianco, nonché la tendenza a risplendere di una flebile luce propria. Dovendo sollevare in volo il peso dell’Aasimar, tali ali sono molto ampie: possono tranquillamente raggiungere i quattro metri d’apertura, quando completamente distese.
N.B. Le ali degli Aasimar sono lievemente più forti e robuste di quelle dei Tiefling.


Altezza: analoga a quella della forma vivente, al massimo maggiorata di qualche centimetro.

Peso: analogo a quello della forma vivente, al massimo maggiorato di qualche chilo.

<b>Vita e riproduzione</b>
Nel momento in cui il Patrono si fonde al corpo del vivente, quest’ultimo risente dell’essenza eterna e immutabile dello spirito che accoglie. Ne risulta che, da che un Aasimar diventa tale, il suo corpo cessa di invecchiare e deperirsi, e diviene sostanzialmente immortale. 
Gli Aasimar possono riprodursi, e nel farlo seguono sostanzialmente le regole della razza a cui appartenevano prima (giacché il sistema riproduttivo e il patrimonio genetico permangono quelli originari). L’unica piccola differenza è che, qualora concepisse in Forma Celestiale, la prole tenderà ad essere di indole buona, e con una particolare affinità agli spiriti dell’Empireo. Senza contare che l’atto del concepimento in sé, in Forma Celestiale, sarebbe senza alcun dubbio un’esperienza quantomeno singolare e bizzarra per il fortuito partner di turno… 

<b>Qualità fisiche peculiari</b>
Cinque sensi: i sensi risultano analoghi a quelli della razza precedente alla trasformazione, ma affinati, migliorati (sebbene non esageratamente: es. un fu-mezzelfo avrà certamente un ottimo olfatto, ma non paragonabile a quello dei mannari). La peculiarità degli Aasimar è che non rimangono abbagliati da forti esposizioni luminose, anzi riescono a vederci perfettamente anche in pienissima luce, quando la retina di chiunque altro risulterebbe accecata. Inoltre, gli Aasimar possono contare su un personalissimo sesto senso, proprio dello spirito che li abita e che dunque li rende sensibili ad altre forme di energia spirituale: essi sono sostanzialmente paragonabili a dei Magister in tal senso (//nel dettaglio, Magister di liv. 3) e sono così in grado di percepire la presenza, la potenza e la natura di un’aura entro 15 metri (per ulteriori approfondimenti sul sesto senso arcano, consultare la sezione “aura” della documentazione inerente alla magia).

Dieta: per rinvigorire il corpo vivente, la dieta è analoga a quella della razza a cui si apparteneva in precedenza. A volte, tuttavia, anche il Patrono necessita di essere nutrito a suo modo: esso, si nutre sostanzialmente del bene. Fare buone azioni, che sia proteggere un’intera città da un mostro sputafuoco a cento teste o salvare il micetto di Lady Barbaporro incastrato sul faggio più alto del paese, poco importa: l’importante, è essere motivo del bene di qualcuno. Questo, rinfrancherà il Patrono nascosto nell’Aasimar e lo caricherà di energia spirituale.

Prestazioni fisiche: l’energia spirituale è un toccasana per il corpo dell’Aasimar, giacché si somma alla normale energia vitale ivi già presente. La risultante, è un individuo estremamente prestante: in Forma Celestiale, gli Aasimar sono infatti creature dalla forza spaventosa, secondi solamente ai mannari, e sono anche altrettanto veloci e rapidi nei movimenti, secondi in questo campo solamente ai vampiri. In Forma Vivente le loro capacità si mitigano solo di poco: forza e velocità divengono seconde sia a mannari che vampiri, ma rimangono comunque superiori rispetto a qualsiasi altra razza (fanno eccezione Aasimar un tempo faerie: forza e velocità in forma fatata risultano estremamente ridotte rispetto alla forma umana, solamente di poco superiori rispetto a un qualsiasi faerie). Possono patire il dolore e la fatica, ma entrambe le sensazioni vengono spesso quantomeno mitigate dall’energia spirituale del Patrono, soprattutto qualora sia l’Aasimar stesso a richiamarla. Sono, di conseguenza, molto resistenti, giacché oltre alla propria energia vitale normale possono contare anche su quella dello spirito (che dovrà essere tuttavia ricaricata qualora fosse eccessivamente consumata).  

Resistenza al freddo e al caldo: sono lievemente più resistenti della norma alle temperature estreme, giacché il Patrono che è in loro interviene per meglio regolare l’omeostasi del rispettivo corpo. Tra caldo e freddo, resistono comunque meglio a quest’ultimo, poiché basta loro sprigionare un poco della propria energia spirituale per generare calore.

Resistenza ai veleni e alle malattie: la forza benefica del Patrono che abita il corpo dell’Aasimar consente a quest’ultimo di risultare immune a veleni e malattie biologiche, poiché anche qualora qualcosa intervenisse a inficiare l’energia vitale del corpo vivente, vi sarà sempre l’energia spirituale a sopperire, mantenendo l’Aasimar in perfette condizioni. Solamente i veleni magici che sono dunque in grado di interferire anche con l’energia spirituale possono avere effetto su queste creature (sebbene serva una dose doppia rispetto alla norma).

Guarigione delle ferite: in forma vivente la vulnerabilità alle ferite è la medesima della razza d’appartenenza originaria. In forma celestiale, invece, l’essenza spirituale che emerge, impregnando anche più superficialmente la carne dell’Aasimar, concede al corpo dello stesso una resistenza lievemente maggiore alle lesioni di vario genere (fatta eccezione per armi maledette, che al contrario feriscono più a fondo gli Aasimar). In entrambe le forme, comunque, le ferite tendono a guarire più in fretta del normale, impiegando un terzo del tempo rispetto alla media umana (di nuovo, fanno eccezione le ferite da arma maledetta, che al contrario impiegano il doppio del tempo a guarire).

Morte: un Aasimar non può morire dissanguato o trafitto da una lama nel cuore, poiché anche qualora il suo sangue (custode della sua energia vitale) venisse meno, vi sarebbe l’energia spirituale del Patrono a sopperire per essa finché la parte vivente del corpo non si fosse ristabilita (certamente, tuttavia, entrambi gli eventi lascerebbero l’Aasimar estremamente indebolito, e impiegherebbe almeno un mese intero per riprendere appieno le proprie forze). La morte però può sopraggiungere per decapitazione o per incenerimento del corpo. Un’altra modalità peculiare è la distruzione del marchio che suggella il legame tra vivente e spirito: una volta rotto (es. da una coltellata maledetta al centro del glifo) il Patrono abbandonerà l’Aasimar, che dunque tornerà mortale esattamente come in origine (e a quel punto potrà essere ucciso da qualsiasi ferita letale).

<b>Bonus e Malus</b>

BONUS:
- Talento dell’Empireo: gli Aasimar non solo utilizzano abitualmente l’energia dell’Empireo, ma sono proprio costituiti da essa stessa. Per loro, l’Aithèras dei Patroni è una quotidianità, nessuno la maneggia meglio di loro. Per questo, interpretando studi da Magister fruitori dell’Empireo, gli Aasimar saranno sempre un livello superiore rispetto ai propri pari grado, con statistiche di incantesimi aggiustati di conseguenza (es. un Magister di liv.3 casterà incantesimi potenziati con effetti di liv.4).

- Tocco dell’Angelo: il potere benefico del Patrono che ogni Aasimar custodisce, con la dovuta concentrazione, può essere potenziato o perfino esteso ad altri individui. E’ necessario concentrarsi sull’energia spirituale dentro al rispettivo corpo, prendere contatto e sincronizzarsi con essa [1T - Sincronizzazione]. Dopodiché, risvegliando suddetta energia, l’Aasimar deve incanalarla laddove serve, condensandola su una parte di sé oppure su altre persone (max una persona per turno) [2T - Tocco dell’Angelo]. Per poter agire su altri individui al di fuori di sé, è necessario un minimo contatto fisico. La durata e gli effetti di questa abilità variano in base all’esperienza dell’Aasimar:
Liv. 1: è possibile guarire malattie biologiche lievi-moderate e sanare le ferite di due gradi (ferite lievi e moderate guariranno, ferite gravi o mortali diventeranno lievi o moderate). Max 2 turni.
Liv. 2: è possibile guarire malattie biologiche anche gravi e sanare le ferite di due gradi (ferite lievi e moderate guariranno, ferite gravi o mortali diventeranno lievi o moderate). L’energia benefica è inoltre in grado di fungere da panacea per qualsiasi tipo di veleno. Max 3 turni.
Liv. 3: è possibile guarire malattie biologiche anche gravi e sanare completamente qualsiasi tipo di ferita. L’energia benefica è inoltre in grado di fungere da panacea per qualsiasi tipo di veleno. Max 4 turni.
Liv. 4: è possibile guarire malattie biologiche anche gravi e sanare completamente qualsiasi tipo di ferita. L’energia benefica è inoltre in grado di fungere da panacea per qualsiasi tipo di veleno. A questo livello l’Aasimar è anche abbastanza potente da fare ricrescere gli arti o pezzi del corpo mutilati. Max 4 turni.
Utilizzare questi poteri consuma l’energia dell’Aasimar, in maniera più onerosa tanto più grande il male da sanare. Per questo, tale abilità viene dosata attentamente, e nessuno ne fa un uso smodato, anche perché qualora si dovesse consumare interamente l’energia spirituale del Patrono (evento che comunque richiederebbe un abuso reiterato del suo potere) quest’ultimo cesserebbe di esistere, e l’Aasimar tornerebbe il comune mortale di prima.
N.B. Questo bonus può anche essere utilizzato su se stessi, a patto che malattie, ferite o veleni non mettano l’Aasimar in condizione di non riuscire a concentrarsi e completare la fase di sincronizzazione.

- Ninnananna dell’Etere: le leggende che ruotano attorno agli Aasimar parlano di creature benefiche e concilianti… E in effetti, è molto più vero di quanto si pensi. Essi infatti sono in grado di influenzare attivamente la psiche di un soggetto, buono o cattivo che sia. Nel dettaglio, il proprio potere è in grado di esercitare un effetto calmante particolarmente potente su qualsiasi creatura di Hyle (persone, animali… E anche vampiri). Per poter attivare la propria influenza, l’Aasimar deve innanzitutto concentrarsi sull’Aithéras che lo permea naturalmente, focalizzandosi e sincornizzandosi con l’energia dell’Empireo di cui è, in parte, costituito [1T: Sincronizzazione]. Risvegliata la forza del proprio Patrono, essa potrà essere irradiata all’esterno in forma condensata, determinando i propri effetti [2T: Ninnananna dell’Etere]. Maggiore l’abilità dell’Aasimar, e più intensi saranno gli effetti della sua influenza.
Liv. 1: l’influenza dell’Aasimar è in grado di calmare all’istante chiunque rientri nel proprio raggio d’azione. Stati d’animo come l’angoscia, la paura, la rabbia, il nervosismo o quant’altro verranno spazzati via, rimpiazzati da una sensazione di calma e benessere. Tale benessere è particolarmente intenso: non è semplice quiete, ma un vero e proprio status di beatitudine psicofisica.  Inoltre, toccando qualcuno mentre il proprio potere è attivo, è in grado di far cadere il bersaglio in un sonno estatico (max un soggetto per turno). Raggio 5 metri, max 2 turni (non necessita di mantenimento).
Liv. 2, 3 e 4: l’Aasimar è in grado di instillare benessere negli altri soggetti, e toccando qualcuno mentre il proprio potere è attivo, è in grado di far cadere il bersaglio in un sonno estatico (max un soggetto per turno). Inoltre, nel momento in cui l’Aasimar addormenta il soggetto, concentrandosi su quest’ultimo per qualche minuto ed entrando in connessione col viaggio onirico innescato dal sonno stesso [3T: Lettura], egli è in grado di scoprire e comprendere i desideri più grandi del bersaglio stesso. Tanto più abile l’Aasimar, tanto più approfondita sarà la sua ricerca mentale (//starà comunque al Master decretare l’esito della lettura onirica). Il mantenimento è necessario solamente qualora si intenda leggere la mente del bersaglio, non servirà qualora si voglia solamente lasciare il bersaglio dormire. 
Liv. 2: max 3 turni, raggio 10 metri
Liv. 3: max 4 turni, raggio 15 metri
Liv. 4: max 4 turni, raggio 20 metri

MALUS:
- Armi maledette: gli Aasimar sono sensibili alle ombre, la propria nemesi. Sono, nel dettaglio, più vulnerabili a veleni o incantesimi figli dell’energia delle Malebolge, a patto che chi li plasma sia di livello superiore al loro (altrimenti il malus non si innesca). Altrettanto, ferite inferte da lame maledette fanno più male, più danni e impiegano più tempo a guarire (sempre purché colui che ha maledetto le lame sia di livello superiore rispetto all’Aasimar). 

- Sacralità della Missione: ogni Aasimar nasce con uno scopo ben preciso, che conduce Patrono e vivente a fondersi in un unico essere. La Missione, così la chiamano loro. Ciascun Aasimar è mosso da una missione ben precisa, la quale ricopre un ruolo fondamentale nella vita di ognuno di loro. Questo li rende, in un certo senso, vulnerabili: gli Aasimar sono infatti molto sensibili all’andamento dei propri obiettivi personali, e nel momento in cui il loro perseguimento ne viene intaccato, possono soffrire più del normale. Fallire la propria missione (es. il villaggio sotto sua protezione viene distrutto), è quanto di peggiore possa accadere a un Aasimar: quest’ultimo subirà il colpo in maniera particolarmente dura, cadendo in uno status psico-fisico dannoso e terribilmente sofferente. Alcuni sperimentano stati simili alla depressione, alcuni tendono a volersi isolare e racchiudere in un ostinato e triste eremitaggio, altri invece vedono nell’autoflagellazione una giusta e meritata punizione per le proprie colpe. In generale, comunque, a prescindere dalla reazione del singolo, un Aasimar non vive mai bene questi eventi, e il malessere psichico tende anche a renderli fisicamente meno prestanti. L’unico modo per riprendere a vivere normalmente e uscire da questo abisso morale, è riparare al proprio fallimento.

- Fame angelica: ogni Aasimar è costituito per metà dall’energia di un Patrono, il quale per poter continuare a persistere nel mondo terreno ha bisogno di nutrirsi, al pari di un vivente… Ma in maniera squisitamente “angelica”. Gli spiriti dell’Empireo infatti rinvigoriscono la propria essenza attingendo dal loro relativo riflesso nel piano materiale. In altre parole, per poter sopravvivere nel proprio Aasimar, ogni Patrono deve di tanto in tanto nutrirsi di… Bene. Puro e luminosissimo Bene. L’Aasimar pertanto, a prescindere dalla propria indole e dai propri intenti, è costretto a compiere azioni benevole periodicamente se non vuole vedere il proprio Patrono deperire. Nel momento in cui un Patrono inizia ad essere affamato i suoi poteri s’affievoliscono gradualmente, indebolendosi, e dopo due mesi senza aver compiuto alcun atto benevolo lo spirito che dimora nell’Aasimar potrebbe anche scomparire.


<b>Abilità e caratteristiche speciali</b>
Aura: gli Aasimar, custodendo in sé un Patrono, sono dotati di aura, ossia il riverbero di un tipo ben preciso di Aithéras. Quest’aura è del tutto analoga a quella sviluppata dai Magister, con relativi effetti e qualità, e risplende della medesima energia che permea l’Empireo (per maggiori dettagli sull’aura, vedi sezione dedicata nella documentazione inerente alla magia).

Rapporto vivente-spirito: nel momento in cui si genera un Aasimar, accade che uno stesso corpo ospiti due coscienze differenti. Due coscienze, peraltro, estremamente diverse tra loro, provenienti da mondo diametralmente opposti. Rimane, tuttavia, che tali entità dovranno coesistere nel medesimo involucro per, verosimilmente, l’eternità. E’ dunque normale che le due inizino a dialogare, sebbene in maniera del tutto non canonica e variabile in base all’indole di ogni individuo. Il vivente, in ogni caso, sarà in grado di percepire grossolanamente messaggi empatici vaghi e non verbali da parte dello spirito che ospita, sensazioni istintive interpretabili attraverso l’intuizione, che potranno portare le due coscienze a instaurare un surrogato di bislacco ed anomalo dialogo: lo spirito può ad esempio comunicare al proprio vivente di essere stanco, affamato, sofferente, può avvertirlo quando apprezza una determinata situazione o quando, al contrario, si trova in uno stato di disagio o malessere. Alcuni viventi amano dare ascolto alle esigenze del proprio spirito e ad instaurare con esso un legame più profondo, che si sviluppi anche sul piano mentale, mentre altri si limitano solamente a far sì che il proprio spirito stia bene e rimanga in salute, senza tuttavia rinsaldare alcun tipo di rapporto personale con esso, vedendolo come un’entità troppo distante e diversa da sé per poterci intessere un vero e proprio dialogo empatico.

<b>Gerarchia</b>
Empyrei Proditor (= “Traditore dell’Empireo”, Liv. 0 - Rinnegato): è un evento raro, ma può accadere che, per svariati motivi, un Aasimar venga corrotto sino a discostarsi dalla primordiale natura dell’Empireo (a volte può accadere che anche lo spirito che alberga in lui venga in qualche modo corrotto). L’allontanarsi spiritualmente dall’Empireo nega dunque all’Aasimar negletto l’uso di qualsiasi abilità della propria razza, nonché il normale vigore del proprio corpo: un Empyrei Proditor è infatti più debole, il suo aspetto meno luminoso e le sue ali fragili e grigie. Per tornare nel pieno delle forze è necessario che ritrovi la retta via… O che, al contrario, la corruzione penetri tanto a fondo da rendere lo spirito che è in lui un Demone, convertendo così se stesso in un Tiefling. I Traditori dell’Empireo, comunque, sono molto biasimati dagli altri Aasimar, i quali, qualora non vedessero speranza di redenzione, tendono ad allontanare, emarginare e perfino combattere tutti i parirazza che si discostano dagli intenti e dagli scopi degli altri Patroni.

Empyrei Eques (= “Cavaliere dell’Empireo”, Liv. 1): è l’Aasimar inesperto, la cui unione con un Patrono è ancora fresca e recente. In questo stadio l’individuo deve ancora prendere piena confidenza con l’energia spirituale, conoscerla ed imparare a controllarla al meglio, senza sbavature. Questo è anche il periodo in cui il dualismo tra spirito e vivente si stabilizza, in cui i due assestano la propria simbiosi e l’uno impara a conoscere i bisogni dell’altro.

Empyrei Custos (= “Protettore dell’Empireo”, Liv. 2 - Membro anziano): sono gli Aasimar esperti, quelli in cui spirito e vivente dialogano costantemente nel proprio rapporto empatico e in cui la simbiosi funziona alla perfezione. Essi non solo sono un punto fondamentale nella lotta contro al male, ma si occupano anche di aiutare i più giovani della propria razza a sviluppare le proprie abilità e capacità da essere semi-celestiale.

Empyrei Heros (= “Paladino dell’Empireo”, Liv. 3 - Vice di razza, carica unica): egli è colui che fa le veci del caporazza in sua assenza. Si tratta di un Aasimar estremamente capace, impeccabile nell’uso delle proprie abilità e baluardo emblematico nella battaglia contro le tenebre. 

Empyrei Viator (= “Araldo dell’Empireo”, Liv.4 - Caporazza, carica unica): il caporazza è l’esempio più alto e perfetto di Aasimar. Egli incarna tutte le qualità più alte e onorevoli della razza, si pone ad esempio per tutti i propri simili ed è il primo a spendere tutto se stesso nella missione ultima di estirpare le tenebre dal mondo. L’Araldo, più che come capo, si propone come leader, ossia come guida saggia e affidabile in cui gli Aasimar meno esperti possono trovare consiglio e rassicurazione. Vige un rapporto di reciproco rispetto e fiducia, ed egli è sempre pronto ad ascoltare le osservazioni, i timori o le proposte dei propri simili, anche dei più giovani. L’Empyrei Viator è anche colui che può decretare l’esilio di un altro Aasimar, qualora il suo giudizio lo ritenga necessario.

<b>Società</b>
Non esiste una vera e propria società di Aasimar, giacché sono generalmente creature molto rare, che mai hanno formato dei gruppi sufficientemente ampi e nutriti da possedere un’identità propria ben definita. Può tuttavia accadere che una manciata di Aasimar si ritrovi casualmente nei medesimi luoghi: in tal caso, proprio in virtù della propria indole buona e pacifica, soprattutto nei riguardi di qualcuno altrettanto puro di cuore, non è raro che si aggreghino in piccoli clan, all’interno dei quali solitamente uno di loro viene identificato come leader (generalmente, il più saggio ed esperto del gruppo). La figura del leader, ossia dell’Empyrei Viator (e in parte degli altri membri più anziani), serve sostanzialmente a fornire da un lato una guida ai più giovani, e dall’altro a coordinare eventuali azioni o attività volte a cacciare le tenebre, e nelle quali siano coinvolti più Aasimar. 

RELAZIONI INTERPERSONALI
In linea di massima i diversi clan di Aasimar funzionano molto bene come micro-società, poiché tra i membri della stessa scorrono solitamente ottimi rapporti, basati sul rispetto reciproco e sulla comune solidarietà, che consentono dunque un’eccezionale sinergia interindividuale e un ineccepibile spirito di collaborazione.
Gli Aasimar, data la loro indole, non sono molto avvezzi all’affetto e ai sentimenti mortali, sebbene la loro natura per metà vivente li renda comunque in minima parte suscettibili in tal senso. E’ più facile che quei pochi sentimenti che riescono a provare siano rivolti a qualcuno che percepiscono come simile a loro, e dunque altri Aasimar oppure, più raramente, razze longeve e tendenzialmente positive come ad esempio gli elfi. Inutile dire che, qualora un Aasimar arrivi a considerare qualcuno come un caro amico o, addirittura, a innamorarsi di qualcuno, egli rispetterà e onorerà tale legame al meglio delle proprie possibilità. Un Aasimar non tradisce, mai, ma al contrario tende a proteggere chi gli sta a cuore e a cercare di portare del bene nella vita dei propri affetti. L’unica cosa che può distogliere un Aasimar da un legame che ritiene sincero e importante, è la sacralità della propria Missione.
Per questa ragione, gli Aasimar sono individui marcatamente monogami, che una volta legati a qualcuno difficilmente scinderanno tale vincolo. Sono tuttavia tendenzialmente bisessuali: per loro poco importa il genere e in generale l’aspetto fisico di qualcuno. Ciò che conta davvero, è ciò che si cela nell’anima (questo aspetto dipende tuttavia in parte dal background socio-culturale in cui l’individuo era cresciuto prima di trasformarsi in Aasimar).

RAPPORTI CON LE ALTRE RAZZE
Gli Aasimar tendono a non lasciarsi condizionare dalla razza d’appartenenza, ma si prefiggono come obiettivo quello di giudicare chi si trovano di fronte esclusivamente in base alle rispettive azioni, e non alla foggia delle orecchie o al colore della pelle. Esistono, tuttavia, alcune razze per cui è per loro molto più difficile entrare in sintonia: i drow primi fra tutti. Gli elfi oscuri, cattivi per definizione, raramente riusciranno ad entrare nelle grazie di un Aasimar, e se proprio dovessero riuscire nell’impresa, probabilmente si tratterebbe di drow rinnegati, e dunque desiderosi di gettarsi la propria stessa corrotta natura alle spalle. 
Per il resto, esiste una sola razza con cui un Aasimar non riuscirà mai e poi mai a simpatizzare: i Tiefling. La loro nemesi ancestrale.

